// node modules
import React from "react";
import {
  Divider,
  List,
  ListItem,
  ListItemText,
  ListSubheader,
} from "@material-ui/core";
import { makeStyles } from '@material-ui/core/styles';

// local imports
import { ITaskList } from "../TaskLists";


// --------------------------------------------------------------------------------------------------------------------------------------------------------
// ------------------------------------------------------------------------ Styles ------------------------------------------------------------------------
// --------------------------------------------------------------------------------------------------------------------------------------------------------

const useStyles: any = makeStyles(() => ({
  root: {
    width: 160,
  },
}));


// --------------------------------------------------------------------------------------------------------------------------------------------------------
// ---------------------------------------------------------------- Component Definition ------------------------------------------------------------------
// --------------------------------------------------------------------------------------------------------------------------------------------------------

// props and state types
type Props = {
  taskLists: ITaskList[],
};

// actual component
const TaskListsDrawer: React.FC<Props> = (props) => {
  
  const { taskLists } = props;

  const classes = useStyles();

  return (
    <div className={classes.root}>

      <List
        component="nav"
        subheader={
          <ListSubheader>
            <ListItemText primary="Task Lists"/>
          </ListSubheader>
        }
      >
        <Divider/>
        {taskLists.map((inTaskList) => (
          <ListItem button key={inTaskList._id}>
            <ListItemText primary={inTaskList.title} />
          </ListItem>
        ))}
      </List>

    </div>
  );
};


// --------------------------------------------------------------------------------------------------------------------------------------------------------
// ----------------------------------------------------------------------- Exports ------------------------------------------------------------------------
// --------------------------------------------------------------------------------------------------------------------------------------------------------

export default TaskListsDrawer;


